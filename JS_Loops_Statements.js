// While loop, Do While Loop and For Loop

/*
    Examples of While Loop
*/
        let a=10;

        while (a>0)
        {
        console.log(a);
        a--;
        }
/*
    Examples of Do While Loop
*/

/*
    Example of do While loop
*/

        let b=20;
        do
        {
            console.log(b);
            b--;
        }while(b>0);

/*
    Examples of Printing Even Number
*/

        for(let i=2;i<=10;i+=2)
        {
            console.log(i);
        }